package ex1;

/**
 * Classe de calcul simple
 * @author cmich
 *
 */
public class Calcul {

	/**
	 * effectue l'addition de 2 entiers
	 * @param a integer
	 * @param b integer
	 * @return int
	 */
	public int addition(int a, int b){
		return a+b;
	}
	
	/**
	 * effectue la soustraction de 2 entiers
	 * @param a integer
	 * @param b integer
	 * @return int
	 */
	public int soustraction(int a, int b){
		return a-b;
	}
}
